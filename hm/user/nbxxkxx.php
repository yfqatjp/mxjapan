    <div class="user_19 midd_top20">
      <p>中国经济网北京12月8日讯 专业的新能源汽车综合运营服务商云杉智慧新能源技术有限公司（以下简称“云杉智慧”）今日在北京召开发布会，宣布其“三网一位”出行综合服务平台正式启动。</p>
      <p>自2015年成立以来，云杉智慧一直坚持“车网协同、智能互联和专业运维”的商业模式，推出电动汽车充电线下& 线上服务、电动汽车智能出行服务、电动汽车相关数据服务，提供充电设施及运营解决方案、绿色出行解决方案，逐渐发展成为中国最具优势的绿色出行整体解决方案服务商。</p>
      在一年多的运营探索基础上，云杉智慧正式推出“三网一位”出行综合服务平台，融合充电网、车联网、互联网和停车位，提供大数据挖掘和以大数据为基础做增值服务的智能运营平台。基于“云杉快速充电单元”的规模化，利用综合智能运营平台的优势，为用户提供消费与支付服务、停车充电服务、绿色出行服务。
      <p>云杉智慧为独立的第三方充电设施运营企业，与多家国内外一流的充电设备供应商建立了战略合作关系，其开放式、可兼容的充电业务服务平台，可兼容不同厂家的充电设备，为终端用户提供“一卡在手、随地充电”的良好体验。云杉智慧新能源技术有限公司总裁徐征鹏强调，云杉智慧此次推出的“三网一位”出行综合服务平台是开放性的，欢迎更多的新能源汽车厂商和充电站建设运营厂商跟云杉合作共赢。</p>
      <p>作为专业的新能源汽车充电设施运营商，云杉智慧定义全新城市级充电网络，颠覆快速充电站的单一化：建立综合的新能源汽车广场，打造城市新的标志性充电建筑物，不仅满足智能充电服务，同时提供如咖啡厅，便利店，电影院等生活服务，让充电融入现代生活，并与微修、洗车行业融合，为会员提供更具针对性的服务；推出集装箱公园充电站，用新的科技思维创造新的商业模式，让充电服务转变为人们乐于遇见的美好事物；建设立体停车库，第一层整合快充功能，让每一个立体库都是新能源车主的城市快充站。</p>
